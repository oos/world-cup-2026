BROADCAST_BY_CODE: dict[str, BroadcastCountry] = {
    country["code"]: country for country in BROADCAST_COUNTRIES
}


def list_broadcast_countries() -> list[dict[str, str]]:
    return [
        {
            "code": country["code"],
            "name": country["name"],
            "flag_iso": country["flag_iso"],
            "broadcaster_count": len(country["broadcasters"]),
            "has_rights": bool(country["broadcasters"]),
        }
        for country in sorted(BROADCAST_COUNTRIES, key=lambda item: item["name"])
    ]


def get_broadcast_country(country_code: str | None) -> dict | None:
    if not country_code:
        return None
    country = BROADCAST_BY_CODE.get(country_code.strip().lower())
    if not country:
        return None
    return {
        **country,
        "last_updated": LAST_UPDATED,
        "fifa_broadcasts_url": FIFA_BROADCASTS_URL,
    }
